<section>
    <h1> Why Should You Join A Biomedical Engineering Student Group? </h1>
    <h2> What is Biomedical Engineering? </h2>

    <p>
        Biomedical engineering is an advanced engineering discipline that
        combines the knowledge of engineering, biology, and medicine, to
        improve human health through cross-disciplinary activities that
        integrate the engineering science with biomedical sciences and clinical
        practice.  Some examples of well-established areas within the
        biomedical engineering field are bioinstrumentation, biomechanics,
        biomaterials, system physiology, clinical engineering, rehabilitation
        engineering.
        (Section Info Source: Imperial College London)
    </p>

    <h2> Sounds cool. But I don’t study STEM subjects, why should I care about this? </h2>

    <p>
        Because healthcare itself, is a huge and important business sector. And
        the biomedical engineering related sub-sector is showing a high growth
        potential. The healthcare sector is also often seen as a
        "counter-cyclical" industry, as it can usually retain stability during
        economic recessions.
    </p>

    <p>
        Over the past 10 years, the S&P 500 Healthcare
        Index went from 306.45 on Jun. 30, 2009 to 1071.79 as of Jul. 15, 2019,
        up 249.74% throughout the decade. The healthcare sector index
        outperformed most of the other sectors such as energy, financials,
        utilities, as well as the S&P 500 index itself.
    </p>

    <div class="img-pair">
        <img src="images/sp500.jpg" />
        <img src="images/sector.jpg" />
    </div>

    <p>
        One reason for the substantial growth is the dramatically increasing
        global healthcare expenditure. The global healthcare expenditure is
        expected to reach US $10.059 trillion by 2022, with an annual growth
        rate of 2.9% between 2013-2017, and 5.4% between 2018-2022. In the
        United States, the national healthcare expenditure went up to US $3.65
        trillion in 2018, added up to 17.14% of the country’s GDP.
    </p>

    <p>
        While it is good to see a strong-growing sector in the economy, the
        increasing cost of high-quality healthcare to individuals also raises
        people’s concern in the accessibility and sustainability in the
        healthcare industry. The healthcare industry has been addressing this
        issue by shifting the focus from "sick" care to truly "health" care,
        which means "well-being", "prevention", and "early intervention" are
        becoming the priority. Healthcare technologies / advanced medical
        devices are playing a very important role in this transition, as their
        sensors and biometrics can provide a tremendous amount of valuable data
        for healthcare providers such that more resources can be allocated to
        places that matter the most, and can improve the healthcare quality and
        patient experience. The global healthcare technology sector is expected
        to reach US $280 billion in 2021, with compound annual growth rate at
        15.9% between 2016-2021. Such growth potential boosts M&A deal
        activities dramatically. Private equity deal volume increased by almost
        20% year over year, to 316 transactions in 2018; total disclosed deal
        value grew by almost 50%, from US $42.6 billion in 2017 to US $63.1
        billion in 2018. Average deal value in United States reached a
        record-high to over US $400 in 2018. The opportunities available in
        corporate development, banking, accounting, consulting related to
        healthcare sector is truly phenomenal.
    </p>

    <p>
        UA Biomed is a one-of-a-kind student group at the University of
        Alberta, we have students from engineering, science, medicine and
        business to develop innovative biomedical engineering devices. By
        joining the group, non-STEM students will have the opportunity to work
        on a project with some of the best STEM minds on campus on a weekly
        basis. You will develop numerous transferable skills such as teamwork,
        communication, and public speaking, which will be very beneficial to
        your career development. In addition, since we are specializing in
        medical devices development, you will have the opportunity to learn
        about medical research and you can even do your own research in the
        healthcare market. You will build your competitive advantage in the
        industry and ultimately increase your chance market.
    </p>

