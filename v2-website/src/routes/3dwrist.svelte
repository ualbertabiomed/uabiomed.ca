            The team has also been designing the supporting structure/actuator system (“test rig”) for generating and controlling flexion/extension of the wrist model. The team aims to significantly reduce costs of the test rig compared to other similar structures found in relevant literature by designing and 3D printing much of the necessary hardware in-house and utilizing  a system of pulleys, cables and gears to minimize the number of motors required for controlling the wrist model motion. A gear-based single-motor model and a self-supporting pulley-based model are two preliminary designs that the team plans to further design, build and test in Summer and Fall 2021.<br><br>

            The team looks forward to using Summer and Fall 2021 to begin initial 3D prints of the wrist model and test rig hardware for preliminary testing and design iteration, leaving a broad range of opportunities for students to get involved with CAD modeling, 3D printing, component and system testing, instrumentation, wrist kinematics and data collection/analysis, and more!            
