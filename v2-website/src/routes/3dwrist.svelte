    <h2> 3D Wrist Project </h2>
    <div class="hor">
        <p>
            The objective of the 3D Wrist Project is to design and develop a 3D-printed synthetic wrist model, composed of synthetic bone and ligaments with similar anatomical and biomechanical properties as those of a human wrist. The developed 3D printed wrist model will be used for orthopedic biomechanics research; specifically, the team is working on developing a four-corner fusion model which is a common surgical procedure performed to alleviate wrist pain and improve grip strength, all while preserving some range of motion in patients with degenerative conditions of the wrist. In the future, models of other common fusing techniques will be developed for comparison of various metrics between models to evaluate different fusing techniques, ultimately to try and improve clinical outcomes of patients with degenerative conditions of the wrist.<br><br>

            In Winter 2021, the team developed initial SolidWorks CAD models of the wrist bones and ligaments. As well, a flowerplate model is being developed for the wrist model as it is a common tool used to connect carpal bones in the 4-corner fusion procedure. ABS or PLA 3D printing material will be used for the carpal bones to produce material properties similar to healthy human bones, while a pliable material such as Ninja- or Poly-flex will be used to 3D print the wrist ligaments to simulate their biomechanical properties.
        </p>

